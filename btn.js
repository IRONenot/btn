const btn_1 = document.getElementById("btn_1")

let curColor_1 = "red";

btn_1.addEventListener("click", function onClick() {
  if (curColor_1 === "red") {
    curColor_1 = "maroon";
  } else {
    curColor_1 = "red";
  }
  btn_1.style.backgroundColor = curColor_1;
});

